    Chess960StartingPositions(int index, String fenString) {
        this.index = index;
        this.fenString = fenString;
        position = new FENPositionTranslatorImpl().toFENPosition(fenString).createPosition();
        blackKingSide = CastlingConfigurationImpl.getConfiguration(position.board(), KING_SIDE, BLACK);
        blackQueenSide = CastlingConfigurationImpl.getConfiguration(position.board(), QUEEN_SIDE, BLACK);
        whiteKingSide = CastlingConfigurationImpl.getConfiguration(position.board(), KING_SIDE, WHITE);
        whiteQueenSide = CastlingConfigurationImpl.getConfiguration(position.board(), QUEEN_SIDE, WHITE);

        collection = List.of(whiteKingSide, whiteQueenSide, blackKingSide, blackQueenSide);
    }

    private static final Map<Integer, Position<Field, Piece<Field>>> CHESS_960_START_POSITION_LOOKUP_MAP;

    static {
        Map<Integer, Position<Field, Piece<Field>>> wrk = new HashMap<>(960);
        FENPositionTranslator<Field, Piece<Field>> translator = new FENPositionTranslatorImpl();

        for (Chess960StartingPositions chess960StartingPositions : Chess960StartingPositions.values()) {
            Position<Field, Piece<Field>> position = translator.toFENPosition(chess960StartingPositions.fenString).createPosition();
            wrk.put(chess960StartingPositions.index, position);
        }
        CHESS_960_START_POSITION_LOOKUP_MAP = Map.copyOf(wrk);
    }


